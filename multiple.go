package main

import "fmt"

func main() {
	fmt.Println(1 + 1)
	fmt.Println(1 - 1)
	fmt.Println(10 / 2)
	fmt.Println(10 / 3)
	fmt.Println(10.0 / 2)
	fmt.Println(10.0 / 3)
	fmt.Println(10 / 2.0)
	fmt.Println(10 / 3.0)
	fmt.Println(10 % 2)

	fmt.Println(1 << 0)
	fmt.Println(1 << 1)
	fmt.Println(1 << 2)
	fmt.Println(2 >> 1)
}
